def parse_date(date_str):
    """Парсинг даты в любом формате"""
    import re
    # Убираем все нецифровые символы
    cleaned = re.sub(r'[^\d]', '', date_str)

    if len(cleaned) != 8:
        return None

    day = int(cleaned[0:2])
    month = int(cleaned[2:4])
    year = int(cleaned[4:8])

    # Валидация
    if day < 1 or day > 31 or month < 1 or month > 12 or year < 1900 or year > 2100:
        return None

    return day, month, year

def reduce_to_single(num):
    """Сведение числа к простому (1-9)"""
    while num > 9:
        num = sum(int(d) for d in str(num))
    return num

def calculate_numbers(day, month, year):
    """Расчёт всех чисел по Санкхья-Шастре"""
    # Джива = день (простое число)
    jiwa = reduce_to_single(day)

    # Дхарма = Джива + месяц (оба простые)
    month_simple = reduce_to_single(month)
    dharma = reduce_to_single(jiwa + month_simple)

    # Карма = Дхарма + год (оба простые)
    year_simple = reduce_to_single(year)
    karma = reduce_to_single(dharma + year_simple)

    # Прогноз = Джива + 2026
    year_2026 = reduce_to_single(2 + 0 + 2 + 6)  # = 1
    forecast = reduce_to_single(jiwa + year_2026)

    return jiwa, dharma, karma, forecast


